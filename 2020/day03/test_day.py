import unittest

import day


class Test(unittest.TestCase):

    def test_solve_part1(self):
        lines = [
            "..##.......",
            "#...#...#..",
            ".#....#..#.",
            "..#.#...#.#",
            ".#...##..#.",
            "..#.##.....",
            ".#.#.#....#",
            ".#........#",
            "#.##...#...",
            "#...##....#",
            ".#..#...#.#",
        ]

        self.assertEqual(7, day.solve_part1(lines))

    def test_solve_part2(self):
        lines = [
            "..##.......",
            "#...#...#..",
            ".#....#..#.",
            "..#.#...#.#",
            ".#...##..#.",
            "..#.##.....",
            ".#.#.#....#",
            ".#........#",
            "#.##...#...",
            "#...##....#",
            ".#..#...#.#",
        ]

        self.assertEqual(336, day.solve_part2(lines))
